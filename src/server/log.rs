#[no_mangle]
pub unsafe extern "C" fn _log(
    mut log_file: *mut FILE,
    mut format: *const libc::c_char,
    mut args: ...
) {
    let mut epoch: time_t = 0;
    let mut now: *mut tm = 0 as *mut tm;
    let mut arg_list: ::core::ffi::VaListImpl;
    epoch = time(0 as *mut time_t);
    now = localtime(&mut epoch);
    fprintf(
        log_file,
        b"%04d-%02d-%02d %02d:%02d:%02d: \0" as *const u8 as *const libc::c_char,
        (*now).tm_year + 1900 as libc::c_int,
        (*now).tm_mon + 1 as libc::c_int,
        (*now).tm_mday,
        (*now).tm_hour,
        (*now).tm_min,
        (*now).tm_sec,
    );
    arg_list = args.clone();
    vfprintf(log_file, format, arg_list.as_va_list());
    fprintf(log_file, b"\n\0" as *const u8 as *const libc::c_char);
    fflush(log_file);
}
